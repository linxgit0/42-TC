#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		size;
	int		i;
	char	*result;

	size = 0;
	while (s[size])
		size++;
	result = malloc(sizeof(char) * size + 1);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
